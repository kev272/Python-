#A program to return the area of a trapezium  0.5(length1 + length2)h

length1=int(input("Enter the length of the first side: "))
length2=int(input("Enter the length of the second side: "))
h=int(input("Enter the height : "))

area=(0.5*(length1+length2)*h)
print("The area of the trapezium is: ",area)